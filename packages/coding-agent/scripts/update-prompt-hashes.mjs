#!/usr/bin/env node

import { createHash } from "node:crypto";
import { readFileSync, readdirSync, writeFileSync } from "node:fs";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const promptsDir = resolve(__dirname, "../src/core/carter_kit/prompts");
const hashesTomlPath = join(promptsDir, "hashes.toml");
const embeddedTsPath = join(promptsDir, "embedded.ts");

const args = new Set(process.argv.slice(2));
const checkOnly = args.has("--check");
const HASH_SCHEME = "sha3-256-trunc12";

function hashContent(content) {
	return createHash("sha3-256").update(content, "utf8").digest("hex").slice(0, 12);
}

function listTemplateFiles() {
	return readdirSync(promptsDir)
		.filter((name) => name.endsWith(".md"))
		.sort((a, b) => a.localeCompare(b));
}

function loadTemplates(templateFiles) {
	return new Map(templateFiles.map((name) => [name, readFileSync(join(promptsDir, name), "utf8")]));
}

function buildHashMap(templates) {
	return new Map([...templates.entries()].map(([name, content]) => [name, hashContent(content)]));
}

function parseHashMap(tomlText) {
	const map = new Map();
	for (const line of tomlText.split("\n")) {
		const match = line.match(/^"([^"]+)"\s*=\s*"([0-9a-f]+)"\s*$/);
		if (match) {
			map.set(match[1], match[2]);
		}
	}
	return map;
}

function renderToml(hashMap) {
	const names = [...hashMap.keys()].sort((a, b) => a.localeCompare(b));
	const maxLen = names.reduce((max, name) => Math.max(max, name.length), 0);
	const entries = names
		.map((name) => {
			const key = `"${name}"`;
			const padding = " ".repeat(maxLen - name.length + 1);
			return `${key}${padding}= "${hashMap.get(name)}"`;
		})
		.join("\n");

	return `# Prompt template hash registry
# ============================================================
# Scheme: ${HASH_SCHEME}
#   SHA3-256 of the UTF-8 file content, first 12 hex characters.
#
# Update after editing prompt templates:
#   npm run update-prompt-hashes --workspace @punkin-pi/coding-agent
#
# Validate hashes without writing:
#   npm run check-prompt-hashes --workspace @punkin-pi/coding-agent
# ============================================================

scheme = "${HASH_SCHEME}"

[templates]
${entries}
`;
}

function renderEmbedded(hashMap, templates) {
	const names = [...templates.keys()].sort((a, b) => a.localeCompare(b));
	const templateObj = Object.fromEntries(names.map((name) => [name, templates.get(name)]));
	const hashObj = Object.fromEntries(names.map((name) => [name, hashMap.get(name)]));
	const templateJson = JSON.stringify(templateObj, null, "\t");
	const hashJson = JSON.stringify(hashObj, null, "\t");

	return `/**
 * Auto-generated by scripts/update-prompt-hashes.mjs.
 * Do not edit by hand.
 */

export const EMBEDDED_HASH_SCHEME = "${HASH_SCHEME}" as const;

export const EMBEDDED_HASH_REGISTRY = {
\tscheme: EMBEDDED_HASH_SCHEME,
\ttemplates: ${hashJson},
} as const;

export const EMBEDDED_PROMPT_TEMPLATES = ${templateJson} as const;
`;
}

function findMismatches(expected, actual) {
	const changes = [];
	const keys = new Set([...expected.keys(), ...actual.keys()]);
	for (const key of [...keys].sort((a, b) => a.localeCompare(b))) {
		const before = expected.get(key);
		const after = actual.get(key);
		if (before !== after) {
			changes.push({ key, before, after });
		}
	}
	return changes;
}

const templateFiles = listTemplateFiles();
const templates = loadTemplates(templateFiles);
const nextHashes = buildHashMap(templates);
const nextToml = renderToml(nextHashes);
const nextEmbeddedTs = renderEmbedded(nextHashes, templates);

let currentToml = "";
try {
	currentToml = readFileSync(hashesTomlPath, "utf8");
} catch {
	currentToml = "";
}

let currentEmbeddedTs = "";
try {
	currentEmbeddedTs = readFileSync(embeddedTsPath, "utf8");
} catch {
	currentEmbeddedTs = "";
}

if (checkOnly) {
	if (currentToml === nextToml && currentEmbeddedTs === nextEmbeddedTs) {
		console.log("Prompt hashes are up to date.");
		process.exit(0);
	}

	if (currentToml !== nextToml) {
		const currentHashes = parseHashMap(currentToml);
		const mismatches = findMismatches(currentHashes, nextHashes);
		console.error("Prompt hashes are out of date:");
		for (const mismatch of mismatches) {
			if (mismatch.before === undefined) {
				console.error(`  + ${mismatch.key}: ${mismatch.after}`);
			} else if (mismatch.after === undefined) {
				console.error(`  - ${mismatch.key}: ${mismatch.before}`);
			} else {
				console.error(`  ~ ${mismatch.key}: ${mismatch.before} -> ${mismatch.after}`);
			}
		}
	}
	if (currentEmbeddedTs !== nextEmbeddedTs) {
		console.error("Embedded prompt bundle is out of date.");
	}
	console.error(
		"\nRun: npm run update-prompt-hashes --workspace @punkin-pi/coding-agent",
	);
	process.exit(1);
}

if (currentToml === nextToml && currentEmbeddedTs === nextEmbeddedTs) {
	console.log("Prompt hashes already up to date.");
	process.exit(0);
}

writeFileSync(hashesTomlPath, nextToml, "utf8");
writeFileSync(embeddedTsPath, nextEmbeddedTs, "utf8");
console.log(`Updated ${hashesTomlPath} (${nextHashes.size} templates).`);
console.log(`Updated ${embeddedTsPath}.`);
